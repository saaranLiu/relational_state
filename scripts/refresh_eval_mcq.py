# ---------------------------------------------------------------------------
# Entry
# ---------------------------------------------------------------------------

def main() -> None:
    args = parse_args()
    src = Path(args.input_file)
    dst = Path(args.output_file) if args.output_file else src

    payload = json.loads(src.read_text(encoding="utf-8"))
    split = payload.get("split") or src.stem
    records: List[Dict[str, Any]] = payload.get("records", [])
    if not records:
        print(json.dumps({"status": "empty", "input": str(src)}), flush=True)
        return

    is_placebo = split == "placebo_test"
    refresh_fn = _refresh_record_placebo if is_placebo else _refresh_record_positional

    # Stable ordering so round-robin letter cycling is deterministic.
    records_sorted = sorted(records, key=lambda r: r["record_id"])
    rng = random.Random(args.seed + (70000 if is_placebo else 60000))
    letter_cycle = ("A", "B", "C", "D")

    stats = {
        "total": 0,
        "degraded_ident_count": 0,
        "not_stratified_count": 0,
        "pairwise_not_ok_count": 0,
    }

    for i, rec in enumerate(records_sorted):
        ensure_oracle(rec)  # sanity
        letter = letter_cycle[i % 4]
        flags = refresh_fn(rec, rng, letter)
        stats["total"] += 1
        if flags["degraded_ident"] == "True":
            stats["degraded_ident_count"] += 1
        if flags["stratified"] != "True":
            stats["not_stratified_count"] += 1
        if flags["pairwise_ok"] != "True":
            stats["pairwise_not_ok_count"] += 1

    payload["records"] = records
    payload["mcq_refresh_last_run_utc"] = datetime.now(timezone.utc).isoformat()
    payload["mcq_refresh_stats"] = stats
    payload["mcq_refresh_seed"] = args.seed

    dst.parent.mkdir(parents=True, exist_ok=True)
    dst.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")

    print(json.dumps({
        "status": "refreshed",
        "split": split,
        "input": str(src),
        "output": str(dst),
        **stats,
    }, ensure_ascii=False, indent=2), flush=True)


if __name__ == "__main__":
    main()
